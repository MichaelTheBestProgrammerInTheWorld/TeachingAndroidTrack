package conditions;

public class IfElseMultiple {

    public static void main(String[] args) {

        int x = 50;

        if (x==10){
            System.out.println("x is 10");
        } else if (x==20){
            System.out.println("x is 20");
        } else if (x==30){
            System.out.println("x is 30");
        } else if (x==40){
            System.out.println("x is 40");
        } else if (x==50){
            System.out.println("Yes you made it");
        }
    }
}
